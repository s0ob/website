			<h2 class="mg">Overwatch win the match!</h2>
                        <p>Overwatch currently boasts the largest share of players at Korean internet cafes (PC bangs), according to Gametrics.</p>
                        <p>Gametrics,which tracks and charts what people are playing at more than 4,000 of the country's PC bangs, lists Overwatch as the game of choice for more than 30 percent of cafe patrons, besting League of Legends' 27.8 percent share.</p>

                        <p>That's a major achievement for the game, considering League of Legends' longtime popularity with Korean players, many of whom do the majority of their gaming in PC bangs. Prior to this week, League has kept the top spot since August 2012. Overwatch marks the first game in nearly four years to unseat Riot Games' popular multiplayer online battle arena title.</p>

                        <p>Blizzard Entertainment has long held a special place in the hearts of Korean gamers, thanks to the enduring success of StarCraft. The sci-fi strategy game has been one of the country's biggest hits since it launched in 1998, spawning professional tournaments with well-known players. That includes Overwatch star D.Va who, according to the game's lore, is a top-ranked StarCraft player before joining the newest franchise's cast.</p>

                        <p>Overwatch will soon get a competitive mode of its own, which Korean players were the first to try out. That will roll out to everyone later this month.</p>

                        <p>We've reached out to Blizzard to confirm Gametrics' statistics and will update with more when we receive it.</p>
